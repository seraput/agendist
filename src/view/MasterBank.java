    private void bt_simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_simpanActionPerformed
        if (kode_bank.getText().isEmpty() || nama_bank.getText().isEmpty() || cb_jenis.getSelectedItem().equals("Pilih") || cb_status.getSelectedItem().equals("Pilih")) {
            JOptionPane.showMessageDialog(null, "Data Belum Lengkap!", "Perhatian!", HEIGHT, warning);
        } else {
            try {
                mbm.fun_Simpan(this);
            } catch (SQLException ex) {
                Logger.getLogger(MasterBank.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_bt_simpanActionPerformed

